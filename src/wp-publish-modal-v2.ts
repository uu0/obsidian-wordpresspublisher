  private doPublish(params: WordPressPostParams, btn?: HTMLButtonElement): void {
    // 防止重复点击
    if (this.isPublishing) {
      log.info('Already publishing, ignoring click');
      return;
    }

    this.isPublishing = true;
    // 保存参数以便重试
    this.lastPublishParams = params;

    // Pass edited content via params, NOT via frontmatter
    params.content = this.editableContent;

    // 同步可编辑标签到发布参数
    if (this.editableTags.length > 0) {
      params.tags = [...this.editableTags];
    }

    // 隐藏模态窗口，显示全屏进度条
    this.modalEl.style.display = 'none';
    const progressOverlay = this.showPublishProgress();

    const doSubmit = () => {
      if (this.matterData.postType
        && this.matterData.postType !== PostTypeConst.Post
        && (this.matterData.tags || this.matterData.categories)
      ) {
        return openConfirmModal({
          message: this.t('publishModal_wrongMatterDataForPage')
        }, this.plugin)
          .then(result => {
            if (result.code === ConfirmCode.Confirm) {
              return this.onSubmit(params, fm => {
                delete fm.categories;
                delete fm.tags;
              }, this.featuredImage || undefined);
            }
            throw new Error('User cancelled');
          });
      } else {
        return new Promise<void>((resolve, reject) => {
          try {
            this.onSubmit(params, fm => {
              // Save excerpt and tags to frontmatter via callback
              if (params.excerpt) {
                fm.excerpt = params.excerpt;
              }
              if (params.tags && params.tags.length > 0) {
                // 根据用户设置格式化标签
                fm.tags = TagFormatter.formatTags(
                  params.tags,
                  this.plugin.settings.tagFormat
                );
              }
            }, this.featuredImage || undefined);
            resolve();
          } catch (e) {
            reject(e);
          }
        });
      }
    };

    doSubmit()
      .then(() => {
        // 发布成功 - 移除进度条
        progressOverlay.remove();
        // 显示纸屑烟花特效
        this.showConfetti();
        // 显示成功提示
        this.showSuccessNotice();
      })
      .catch(err => {
        log.error('Publish error:', err);
        progressOverlay.remove();
        if (err instanceof Error && err.message !== 'User cancelled') {
          // 显示失败提示卡片
          this.showErrorCard(err.message);
        } else {
          this.close();
        }
        this.isPublishing = false;
      });
  }

