/**
 * Get tag color based on name hash
 */
function getTagColor(tagName: string): string {
    const hash = tagName.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
    return TAG_COLORS[hash % TAG_COLORS.length];
}

/**
 * Detect text language
 */
function detectLanguage(text: string): 'zh' | 'en' | 'other' {
    if (!text || text.length < 10) return 'en';
    const chineseChars = text.match(/[\u4e00-\u9fa5]/g);
    const chineseCount = chineseChars ? chineseChars.length : 0;
    const englishChars = text.match(/[a-zA-Z]/g);
    const englishCount = englishChars ? englishChars.length : 0;
    if (chineseCount > text.length * 0.3) return 'zh';
    if (englishCount > text.length * 0.4) return 'en';
    return 'other';
}

/**
 * Get localized prompt
 */
function getLocalizedPrompt(plugin: WordpressPlugin, language: 'zh' | 'en' | 'other', type: 'summary' | 'tags' | 'image'): string {
    if (type === 'summary' && plugin.settings.summaryPrompt) return plugin.settings.summaryPrompt;
    if (type === 'tags' && plugin.settings.tagsPrompt) return plugin.settings.tagsPrompt;
    if (type === 'image' && plugin.settings.imageGenerationPrompt) return plugin.settings.imageGenerationPrompt;

    if (language === 'en' || language === 'other') {
        if (type === 'summary') return plugin.t('defaultPrompt_summaryEn');
        if (type === 'tags') return plugin.t('defaultPrompt_tagsEn');
        return plugin.t('defaultPrompt_imageEn');
    }

    if (type === 'summary') return plugin.t('defaultPrompt_summary');
    if (type === 'tags') return plugin.t('defaultPrompt_tags');
    return plugin.t('defaultPrompt_image');
}

