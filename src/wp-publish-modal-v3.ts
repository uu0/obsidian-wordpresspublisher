    private renderCategorySelector(container: HTMLElement, params: WordPressPostParams): void {
        const validCategories = this.categories.items.filter(it => it.name && it.name.trim());
        if (validCategories.length === 0) return;

        const wrapper = container.createDiv({ cls: 'setting-item' });
        const info = wrapper.createDiv('setting-item-info');
        info.createDiv('setting-item-name').setText(this.t('publishModal_categoryName'));

        const control = wrapper.createDiv('setting-item-control');
        const tagsContainer = control.createDiv('wp-category-tags-container');

        const renderTags = () => {
            tagsContainer.empty();

            params.categories.forEach(catId => {
                const cat = validCategories.find(c => Number(c.id) === catId);
                if (cat) {
                    const tag = tagsContainer.createEl('span', { cls: 'wp-category-tag', text: cat.name });
                    const removeBtn = tag.createEl('span', { cls: 'wp-category-tag-remove', text: '×' });
                    removeBtn.onclick = () => {
                        params.categories = params.categories.filter(id => id !== catId);
                        renderTags();
                    };
                }
            });

            // Add button
            const available = validCategories.filter(c => !params.categories.includes(Number(c.id)));
            if (available.length > 0) {
                const addBtn = tagsContainer.createEl('button', { cls: 'wp-category-add-btn', text: '+' });
                addBtn.onclick = () => {
                    const select = tagsContainer.createEl('select', { cls: 'wp-category-dropdown' });
                    select.createEl('option', { value: '', text: this.t('publishModal_selectCategory') || 'Select...' });
                    available.forEach(c => select.createEl('option', { value: String(c.id), text: c.name }));
                    select.onchange = () => {
                        if (select.value) {
                            params.categories.push(Number(select.value));
                            renderTags();
                        }
                    };
                    select.focus();
                    addBtn.style.display = 'none';
                };
            }
        };

        renderTags();

        // Default to "Uncategorized"
        if (params.categories.length === 0) {
            const uncategorized = validCategories.find(c =>
                c.name === 'Uncategorized' || c.name === '未分类' || c.name === this.plugin.t('publishModal_uncategorized')
            );
            if (uncategorized) {
                params.categories = [Number(uncategorized.id)];
                renderTags();
            }
        }
    }

