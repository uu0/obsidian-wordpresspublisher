    private display(params: WordPressPostParams): void {
        const { contentEl } = this;
        this.currentParams = params;

        contentEl.empty();
        contentEl.addClass('wp-publish-modal-v3');

        this.createHeader(this.t('publishModal_title'));
        this.updateModalWidth();

        // Main container with two-column layout
        const mainContainer = contentEl.createDiv('wp-layout-container');

        // Left: Preview area
        const previewArea = mainContainer.createDiv('wp-layout-preview');
        this.renderPreviewArea(previewArea, params);

        // Right: Floating panels (desktop) or toggle buttons (mobile)
        const panelsArea = mainContainer.createDiv('wp-layout-panels');
        this.renderPanelsArea(panelsArea, params);

        // Bottom action bar
        this.renderBottomBar(contentEl, params);
    }

    private updateModalWidth(): void {
        const modalEl = this.modalEl;
        if (modalEl) {
            modalEl.style.width = '95vw';
            modalEl.style.maxWidth = '1000px';
            modalEl.style.minWidth = '400px';
        }
    }

